/**
 * Интерфейс, описывающий игрока в системе.
 */
export interface IPlayer {
    /**
     * Уникальный идентификатор пользователя.
     */
    user_id: string;

    /**
     * Игровое имя пользователя, отображаемое в интерфейсе.
     */
    user_game_name: string;

    /**
     * Набор ссылок на аватар игрока в разных разрешениях.
     * - original — оригинальное изображение
     * - x96 — уменьшенная версия (96px)
     * - x48 — уменьшенная версия (48px)
     */
    avatar: {
        original: string;
        x96: string;
        x48: string;
    };

    /**
     * Статус присутствия игрока — онлайн или офлайн.
     */
    isOnline: boolean;
}
