/**
 * Типы данных для сущности "Новость".
 *
 * Содержит DTO для API-контракта и UI-модель для отображения в компонентах.
 */

/**
 * DTO новости, получаемый от API.
 *
 * Строго соответствует protobuf-контракту News.
 * Поля created_at и created_by могут отсутствовать или приходить
 * как в snake_case, так и в camelCase в зависимости от версии бэкенда.
 */
export interface NewsDto {
    /**
     * Уникальный идентификатор новости.
     */
    id: string;

    /**
     * Заголовок новости.
     */
    title: string;

    /**
     * Содержание новости (HTML-разметка).
     */
    content: string;

    /**
     * Превью-изображение.
     *
     * Публичный URL изображения, полученный через MediaService.
     */
    preview: string;

    /**
     * Количество просмотров.
     *
     * Передаётся как строка (int64 as string в protobuf).
     */
    view_count: string;

    /**
     * Дата создания в формате ISO 8601 (snake_case).
     */
    created_at?: string;

    /**
     * Дата создания в формате ISO 8601 (camelCase fallback).
     */
    createdAt?: string;

    /**
     * Идентификатор или имя автора новости (snake_case).
     */
    created_by?: string;

    /**
     * Идентификатор или имя автора новости (camelCase fallback).
     */
    createdBy?: string;
}

/**
 * UI-модель новости.
 *
 * Используется в компонентах для отображения.
 * Содержит отформатированные поля, удобные для шаблонов.
 */
export interface News {
    /**
     * Уникальный идентификатор новости.
     */
    id: string;

    /**
     * Заголовок новости.
     */
    title: string;

    /**
     * Содержание новости (HTML-разметка).
     */
    content: string;

    /**
     * Превью-изображение.
     *
     * Публичный URL изображения.
     */
    preview: string;

    /**
     * Количество просмотров.
     *
     * Преобразовано из строки в число.
     */
    viewCount: number;

    /**
     * Дата создания как объект Date.
     *
     * `null`, если сервер не вернул дату или она невалидна.
     */
    createdAt: Date | null;

    /**
     * Дата создания в человекочитаемом формате.
     *
     * Формат: "DD.MM.YY - HH:mm" или "—", если дата отсутствует.
     */
    formattedDate: string;

    /**
     * Идентификатор или имя автора новости.
     */
    createdBy: string;
}

/**
 * Запрос на создание новости.
 *
 * Отправляется в POST /news.
 * Не содержит полей, которые назначает сервер (id, view_count, created_at).
 */
export interface CreateNewsRequest {
    /**
     * Заголовок новости.
     */
    title: string;

    /**
     * Содержание новости (HTML-разметка).
     */
    content: string;

    /**
     * Превью-изображение.
     *
     * Публичный URL изображения, полученный через MediaService.
     */
    preview: string;
}
