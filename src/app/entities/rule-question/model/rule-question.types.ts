/**
 * Типы данных для сущности "Вопрос правил".
 *
 * Содержит DTO для API-контракта и UI-модель для отображения.
 */

/**
 * DTO вопроса правил, получаемый от API.
 */
export interface RuleQuestionDto {
    /**
     * Уникальный идентификатор вопроса.
     */
    id: string;

    /**
     * Текст вопроса.
     */
    question: string;

    /**
     * Идентификатор пользователя, создавшего вопрос.
     *
     * Присутствует только в списке для администратора.
     */
    created_by?: string;

    /**
     * Дата создания в формате ISO 8601.
     *
     * Присутствует только в списке для администратора.
     */
    created_at?: string;
}

/**
 * UI-модель вопроса правил.
 *
 * Используется в компонентах для отображения.
 */
export interface RuleQuestion {
    /**
     * Уникальный идентификатор вопроса.
     */
    id: string;

    /**
     * Текст вопроса.
     */
    text: string;

    /**
     * Идентификатор пользователя, создавшего вопрос.
     */
    createdBy?: string;

    /**
     * Дата создания.
     */
    createdAt?: Date;
}
