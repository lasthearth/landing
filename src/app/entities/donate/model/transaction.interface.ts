/**
 * DTO транзакции, получаемый от API.
 *
 * Строго соответствует protobuf-контракту Transaction.
 */
export interface ITransactionDto {
    /**
     * Уникальный идентификатор транзакции.
     */
    id: string;

    /**
     * Идентификатор игрока.
     */
    player_id: string;

    /**
     * Сумма транзакции.
     *
     * Передаётся как строка (decimal as string в protobuf).
     */
    amount: string;

    /**
     * Тип транзакции.
     *
     * Например: "CREDIT" (зачисление) или "DEBIT" (списание).
     */
    type: string;

    /**
     * Причина / описание транзакции.
     */
    reason: string;

    /**
     * Идентификатор связанной покупки.
     *
     * Может отсутствовать, если транзакция не связана с покупкой.
     */
    purchase_id?: string;

    /**
     * Дата создания в формате ISO 8601.
     */
    created_at: string;
}

/**
 * UI-модель транзакции.
 *
 * Используется в компонентах для отображения истории транзакций.
 */
export interface ITransaction {
    /**
     * Уникальный идентификатор транзакции.
     */
    id: string;

    /**
     * Идентификатор игрока.
     */
    playerId: string;

    /**
     * Сумма транзакции как строка.
     */
    amount: string;

    /**
     * Тип транзакции.
     */
    type: string;

    /**
     * Причина / описание транзакции.
     */
    reason: string;

    /**
     * Идентификатор связанной покупки.
     *
     * Пустая строка, если транзакция не связана с покупкой.
     */
    purchaseId: string;

    /**
     * Дата создания как объект Date.
     *
     * `null`, если сервер не вернул дату или она невалидна.
     */
    createdAt: Date | null;

    /**
     * Дата создания в человекочитаемом формате.
     *
     * Формат: "DD.MM.YY - HH:mm" или "—", если дата отсутствует.
     */
    formattedDate: string;
}
