/**
 * Элемент списка возможностей (преимуществ) товара.
 *
 * Используется для привилегий и наборов, где каждая возможность
 * отображается в виде иконки с подписью.
 */
export interface IAbilityItem {
    /**
     * Имя иконки в формате Taiga UI, например `@tui.heart`.
     */
    icon: string;

    /**
     * Текст возможности.
     */
    text: string;
}
