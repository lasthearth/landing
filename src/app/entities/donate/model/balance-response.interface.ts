/**
 * DTO ответа на запрос баланса.
 *
 * ⚠️ Структура не документирована в API_CONTRACT.md явно.
 * Предполагается наличие поля balance. Поле player_id добавлено
 * как потенциальное — требуется уточнение у бэкенда.
 */
export interface IBalanceResponseDto {
    /**
     * Текущий баланс игрока в донат-валюте.
     *
     * Передаётся как строка (decimal as string в protobuf).
     * Поле называется `coins` в ответе API.
     */
    coins: string;

    /**
     * Идентификатор игрока.
     *
     * ⚠️ Поле предполагаемое, требует подтверждения от бэкенда.
     */
    player_id?: string;
}

/**
 * UI-модель баланса.
 *
 * Используется в компонентах для отображения текущего баланса донат-валюты.
 */
export interface IBalanceResponse {
    /**
     * Текущий баланс как строка.
     *
     * Не преобразуется в number для сохранения точности decimal.
     */
    coins: string;

    /**
     * Идентификатор игрока.
     */
    playerId: string;
}
