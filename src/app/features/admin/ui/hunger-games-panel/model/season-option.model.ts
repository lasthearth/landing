/**
 * Опция выбора сезона в дропдауне лидерборда.
 *
 * Используется только внутри {@link HungerGamesPanelComponent}.
 */
export interface ISeasonOption {
    /**
     * Идентификатор сезона.
     *
     * Пустая строка соответствует "Текущий сезон".
     */
    id: string;

    /**
     * Отображаемый текст опции.
     */
    label: string;
}
